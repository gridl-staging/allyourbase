func ensureManagedDatabase(ctx context.Context, port uint32, logger *slog.Logger) error {
	db, err := openDatabaseClient(ctx, managedConnURL(port, "postgres"))
	if err != nil {
		return fmt.Errorf("connecting to postgres database: %w", err)
	}
	defer db.Close()

	var exists bool
	if err := db.QueryRowContext(ctx, "SELECT EXISTS (SELECT 1 FROM pg_database WHERE datname = $1)", dbName).Scan(&exists); err != nil {
		return fmt.Errorf("checking for database %s: %w", dbName, err)
	}
	if exists {
		return nil
	}

	logger.Info("creating managed postgres database", "database", dbName, "port", port)
	if _, err := db.ExecContext(ctx, "CREATE DATABASE "+sqlutil.QuoteIdent(dbName)); err != nil {
		return fmt.Errorf("creating database %s: %w", dbName, err)
	}
	return nil
}

// initExtensions runs CREATE EXTENSION IF NOT EXISTS for each configured extension.
// Missing extensions log a warning but do not return an error.
func initExtensions(ctx context.Context, connURL string, extensions []string, logger *slog.Logger) error {
	if len(extensions) == 0 {
		return nil
	}

	db, err := openPgx(ctx, connURL)
	if err != nil {
		return fmt.Errorf("connecting for extension init: %w", err)
	}
	defer db.Close()

	// Query available extensions.
	available := make(map[string]bool)
	rows, err := db.QueryContext(ctx, "SELECT name FROM pg_available_extensions")
	if err != nil {
		return fmt.Errorf("querying pg_available_extensions: %w", err)
	}
	defer rows.Close()
	for rows.Next() {
		var name string
		if err := rows.Scan(&name); err != nil {
			return fmt.Errorf("scanning extension name: %w", err)
		}
		available[name] = true
	}
	if err := rows.Err(); err != nil {
		return fmt.Errorf("iterating pg_available_extensions: %w", err)
	}

	for _, ext := range extensions {
		dbExt := managedExtensionName(ext)
		if !available[dbExt] {
			logger.Warn("extension not available in this PostgreSQL installation — "+
				"connect to an external PostgreSQL with the extension installed, "+
				"or verify the managed PG build includes it",
				"extension", ext)
			continue
		}
		// Extension names are from our config, not user input, so safe to interpolate.
		_, err := db.ExecContext(ctx, "CREATE EXTENSION IF NOT EXISTS "+sqlutil.QuoteIdent(dbExt))
		if err != nil {
			return fmt.Errorf("creating extension %s: %w", ext, err)
		}
		logger.Info("extension initialized", "extension", ext)
	}

	return nil
}

func managedExtensionName(ext string) string {
	switch strings.TrimSpace(strings.ToLower(ext)) {
	case "pgvector", "vector":
		return "vector"
	default:
		return strings.TrimSpace(ext)
	}
}

func managedExtensionNameListContains(values []string, want string) bool {
	for _, value := range values {
		if managedExtensionName(value) == want {
			return true
		}
	}
	return false
}
